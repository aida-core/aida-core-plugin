def find_by_url(
    decisions: List[Decision], url: str
) -> Optional[Decision]:
    """Return the existing decision for ``url``, or None."""
    for d in decisions:
        if d.url == url:
            return d
    return None


def upsert_decision(
    decisions: List[Decision], decision: Decision
) -> List[Decision]:
    """Return ``decisions`` with ``decision`` either appended (new URL)
    or replacing the existing entry for the same URL.

    Pure function; does not mutate the input list. Caller is expected
    to pass the result to :func:`write_decisions` to persist.
    """
    out: List[Decision] = []
    replaced = False
    for d in decisions:
        if d.url == decision.url:
            out.append(decision)
            replaced = True
        else:
            out.append(d)
    if not replaced:
        out.append(decision)
    return out
